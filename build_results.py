"""Generate results.py from results_real.json (real McAuley run), cleaning the
HTML artifacts in the McAuley review text for the EDA word displays."""
import json, re
import pandas as pd
from collections import Counter
from nltk.corpus import stopwords
from sklearn.feature_extraction.text import TfidfVectorizer

CSV = "/Users/luigisalemi/Documents/ADS-509-Project/ADS509_Final_Project/product_reviews_scraped.csv"
d = json.load(open("results_real.json"))

# ---- clean EDA word lists + avg length (strip <br> etc.) ----
raw = pd.read_csv(CSV)
df = raw[raw.label.isin([0, 1])].dropna(subset=["text"]).copy()
n = min(int((df.label == 1).sum()), int((df.label == 0).sum()))
df = pd.concat([df[df.label == 1].sample(n, random_state=42),
                df[df.label == 0].sample(n, random_state=42)]).sample(frac=1, random_state=42).reset_index(drop=True)
df["clean"] = df["text"].map(lambda t: re.sub(r"<[^>]+>", " ", str(t)))
df["rlen"] = df["clean"].str.split().map(len)
avg = df.groupby("label")["rlen"].mean()
avg_len = {"Negative (0)": round(float(avg[0]), 1), "Positive (1)": round(float(avg[1]), 1)}

stop = set(stopwords.words("english"))
toks = re.findall(r"[A-Za-z']{3,}", " ".join(df["clean"]))
words = [w.lower() for w in toks if w.lower() not in stop]
freq = Counter(words)
top20 = [[w, int(c)] for w, c in freq.most_common(20)]
top120 = [[w, int(c)] for w, c in freq.most_common(120)]
tfidf = sorted(TfidfVectorizer(max_features=20, stop_words="english").fit(df["clean"]).get_feature_names_out().tolist())
disc = [w for w in ["great", "love", "quality", "easy", "nice", "good", "work"] if w in tfidf][:5]

lr, db = d["lr"], d["distilbert"]
gain = round((db["accuracy"] - lr["accuracy"]) * 100, 1)
cats = list(d["category_dist"].keys())

def py(x):  # pretty python literal
    return json.dumps(x, indent=4)

